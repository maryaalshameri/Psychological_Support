// Helper functions - تحتاج للتعديل لدعم الترجمة
export const getLawsByCategory = (category, language = 'ar') => {
    const allLaws = []
    Object.values(yemeniLaws).forEach(lawCategory => {
        lawCategory.articles.forEach(article => {
            const articleCategory = typeof article.category === 'object' ? article.category[language] : article.category
            if (articleCategory === category) {
                allLaws.push({
                    ...article,
                    lawTitle: typeof lawCategory.title === 'object' ? lawCategory.title[language] : lawCategory.title
                })
            }
        })
    })
    return allLaws
}

export const searchLaws = (searchTerm, language = 'ar') => {
    const results = []
    Object.values(yemeniLaws).forEach(lawCategory => {
        lawCategory.articles.forEach(article => {
            const articleText = typeof article.text === 'object' ? article.text[language] : article.text
            const articleCategory = typeof article.category === 'object' ? article.category[language] : article.category
            const articleNumber = typeof article.number === 'object' ? article.number[language] : article.number
            
            if (
                articleText.includes(searchTerm) ||
                articleCategory.includes(searchTerm) ||
                articleNumber.includes(searchTerm)
            ) {
                results.push({
                    ...article,
                    lawTitle: typeof lawCategory.title === 'object' ? lawCategory.title[language] : lawCategory.title
                })
            }
        })
    })
    return results
}

export const getLawCategories = (language = 'ar') => {
    const categories = new Set()
    Object.values(yemeniLaws).forEach(lawCategory => {
        lawCategory.articles.forEach(article => {
            const category = typeof article.category === 'object' ? article.category[language] : article.category
            categories.add(category)
        })
    })
    return Array.from(categories)
}

// دالة مساعدة للحصول على النص المترجم
export const getTranslatedLaw = (article, language = 'ar') => {
    return {
        id: article.id,
        number: typeof article.number === 'object' ? article.number[language] : article.number,
        text: typeof article.text === 'object' ? article.text[language] : article.text,
        category: typeof article.category === 'object' ? article.category[language] : article.category
    }
}